"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> String -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Isso é um texto')
print('Isso é um "texto"')  # dica trocar de aspas se quiser usar aspas

# Aspas duplas
print("Isso é um texto")


# Escape
print("Isso é um \"texto\"")  # \ não interpreta o proximo caracteres
# r
print(r"Isso é um \"texto\"")  # r expressões regulares, reparar que o \ aparece
